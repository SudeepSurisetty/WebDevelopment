package Bean;

public class Item {


public Item(){
}


private String name;
private String price;
private int id;

public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public String getPrice(){
    	return price;
    }
    
    public void setPrice(String price){
    	this.price = price;
    }
    
     public int getId(){
    	return id;
    }
    
    public void setId(int id){
    	this.id = id;
    }


}